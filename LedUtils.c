DWORD GetPixelsSize(long cx, long cy)
{
  return sizeof(TPixels)+cx*(((cy+0xf)&(~0xf))>>2);
}

//\u83b7\u5f97\u70b9\u9635\u6570\u636e
//  obuffer\u662f\u8f6c\u6362\u540e\u7684\u70b9\u9635\u6570\u636e
//  ibuffer\u662f\u6e90\u56fe\u7247\u6570\u636e\uff0c\u6570\u636e\u683c\u5f0f\u4e3a\u6bcf\u4e2a\u50cf\u7d20\u70b9\u5305\u542b(RGB)3\u4e2a\u5b57\u8282
//  cx,cy\u662f\u6e90\u56fe\u7247\u7684\u5927\u5c0f
DWORD MakePixels(BYTE* obuffer, BYTE* ibuffer, long cx, long cy)
{
  long x, y;
  PRGB rgb;
  DWORD shift;
  PPixels pixels=(PPixels)obuffer;
  DWORD* ptr=(DWORD*)(obuffer+sizeof(TPixels));
  pixels->id=ID_PIXELS;
  pixels->bits=BITMAP_COLOR;
  pixels->width=cx;
  pixels->height=((cy+0xf)&(~0xf));
  pixels->size=sizeof(TPixels)+pixels->width*(pixels->height>>2);
  rgb=(PRGB)ibuffer;
  *ptr=0;
  for (x=0; x<cx; x++)
  {
    shift=0x80000000;
    for (y=0; y<cy; y++)
    {
      if (rgb->g>0) (*ptr)|=shift;
      shift>>=1;
      if (rgb->r>0) (*ptr)|=shift;
      shift>>=1;
      if (shift==0)
      {
        shift=0x80000000;
        ptr++;
        *ptr=0;
      }
      rgb=(PRGB)((DWORD)rgb+3);
    }
    if (shift!=0x80000000)
	{
	  ptr++;
	  *ptr=0;
	}
  }
  return pixels->size;
}

