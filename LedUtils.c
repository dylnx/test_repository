void EncodeTimeStamp(int year, int month, int day, int hour, int minute, int second, PTimeStamp otime)
{
  DWORD MonthDays[2][12]={{31,28,31,30,31,30,31,31,30,31,30,31},{31,29,31,30,31,30,31,31,30,31,30,31}};
  long  i,y,m,d;

  y=year-1;
  for (m=0,d=year; d>100; d-=100, m++) ;

  if (((year & 3)==0) && ((m & 3)==0 || (d!=0))) d=1;
  else d=0;

  otime->date=day;
  for (i=1; i<=month-1; i++) otime->date+=MonthDays[d][i-1];
  otime->date+=y*365+(y>>2)-(y/100)+(y/400);

  otime->time=hour*60*60000+minute*60000+second*1000;
}

